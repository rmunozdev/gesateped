package pe.com.gesateped.model;

public class UbicacionPedido {

	private String codigoBodega;
	private String codigoPedido;
	private String codigoProducto;
	private String areaBodega;
	private String pasiArea;
	private String ladoPasillo;
	private String alturaLado;
	private String profAltu;
	
	public String getCodigoBodega() {
		return codigoBodega;
	}
	public void setCodigoBodega(String codigoBodega) {
		this.codigoBodega = codigoBodega;
	}
	public String getCodigoPedido() {
		return codigoPedido;
	}
	public void setCodigoPedido(String codigoPedido) {
		this.codigoPedido = codigoPedido;
	}
	public String getCodigoProducto() {
		return codigoProducto;
	}
	public void setCodigoProducto(String codigoProducto) {
		this.codigoProducto = codigoProducto;
	}
	public String getAreaBodega() {
		return areaBodega;
	}
	public void setAreaBodega(String areaBodega) {
		this.areaBodega = areaBodega;
	}
	public String getPasiArea() {
		return pasiArea;
	}
	public void setPasiArea(String pasiArea) {
		this.pasiArea = pasiArea;
	}
	public String getLadoPasillo() {
		return ladoPasillo;
	}
	public void setLadoPasillo(String ladoPasillo) {
		this.ladoPasillo = ladoPasillo;
	}
	public String getAlturaLado() {
		return alturaLado;
	}
	public void setAlturaLado(String alturaLado) {
		this.alturaLado = alturaLado;
	}
	public String getProfAltu() {
		return profAltu;
	}
	public void setProfAltu(String profAltu) {
		this.profAltu = profAltu;
	}
	
	
}
