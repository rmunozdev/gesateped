package pe.com.gesateped.model;

public class Tienda {

	private String codigo;
	private String nombre;
	private String direccion;
	private String codigoController;
	private String codigoDistrito;
	
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getCodigoController() {
		return codigoController;
	}
	public void setCodigoController(String codigoController) {
		this.codigoController = codigoController;
	}
	public String getCodigoDistrito() {
		return codigoDistrito;
	}
	public void setCodigoDistrito(String codigoDistrito) {
		this.codigoDistrito = codigoDistrito;
	}
	
	
	
}
